#[derive(Clone, Copy)]
struct NoonTest;
impl ConnectorActions for NoonTest {}
impl utils::Connector for NoonTest {
        /// Retrieves the connector data for the Noon API, including the connector instance, name, token retrieval method, and merchant connector ID.
    fn get_data(&self) -> types::api::ConnectorData {
        use router::connector::Noon;
        types::api::ConnectorData {
            connector: Box::new(&Noon),
            connector_name: types::Connector::Noon,
            get_token: types::api::GetToken::Connector,
            merchant_connector_id: None,
        }
    }

        /// Retrieves the authentication token for the connector. 
    /// 
    /// # Returns
    /// 
    /// The authentication token for the connector as a `ConnectorAuthType`.
    fn get_auth_token(&self) -> types::ConnectorAuthType {
        utils::to_connector_auth_type(
            connector_auth::ConnectorAuthentication::new()
                .noon
                .expect("Missing connector authentication configuration")
                .into(),
        )
    }

        /// This method returns the name "noon" as a String.
    fn get_name(&self) -> String {
        "noon".to_string()
    }
}

static CONNECTOR: NoonTest = NoonTest {};

/// Retrieves the default payment information, if available.
