// Voids a payment using automatic capture flow (Non 3DS).
#[actix_web::test]
/// This method attempts to make a void payment for an auto-captured transaction. 
/// It first makes a payment using the provided payment method details and default payment info, 
/// then checks if the payment was successfully authorized and gets the transaction ID. 
/// Using the obtained transaction ID, it then attempts to void the payment and asserts that 
/// the response contains an error message indicating that the PaymentIntent cannot be canceled 
/// because it has a status of succeeded.
async fn should_fail_void_payment_for_auto_capture() {
    let authorize_response = CONNECTOR
        .make_payment(payment_method_details(), get_default_payment_info())
        .await
        .unwrap();
    assert_eq!(authorize_response.status, enums::AttemptStatus::Charged);
    let txn_id = utils::get_connector_transaction_id(authorize_response.response);
    assert_ne!(txn_id, None, "Empty connector transaction id");
    let void_response = CONNECTOR
        .void_payment(txn_id.unwrap(), None, get_default_payment_info())
        .await
        .unwrap();
    assert_eq!(
        void_response.response.unwrap_err().message,
        "You cannot cancel this PaymentIntent because it has a status of succeeded."
    );
}

