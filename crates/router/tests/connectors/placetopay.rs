use crate::utils::{self, ConnectorActions};

#[derive(Clone, Copy)]
struct PlacetopayTest;
impl ConnectorActions for PlacetopayTest {}
impl utils::Connector for PlacetopayTest {
        /// This method returns a ConnectorData object containing information about the Placetopay connector.
        fn get_data(&self) -> types::api::ConnectorData {
            use router::connector::Placetopay;
            types::api::ConnectorData {
                connector: Box::new(&Placetopay),
                connector_name: types::Connector::Placetopay,
                get_token: types::api::GetToken::Connector,
                merchant_connector_id: None,
            }
        }

        /// Retrieves the authentication token for the connector.
    /// 
    /// This method returns the authentication token for the connector by converting the connector authentication type
    /// to the appropriate format using the `to_connector_auth_type` function from the `utils` module.
    fn get_auth_token(&self) -> types::ConnectorAuthType {
        utils::to_connector_auth_type(
            connector_auth::ConnectorAuthentication::new()
                .placetopay
                .expect("Missing connector authentication configuration")
                .into(),
        )
    }

        /// This method returns the name "placetopay".
    fn get_name(&self) -> String {
        "placetopay".to_string()
    }
}

static CONNECTOR: PlacetopayTest = PlacetopayTest {};

/// Retrieves the default payment information, if available.
///
/// If the default payment information is available, it will be returned as Some(PaymentInfo).
/// If the default payment information is not available, None will be returned.
