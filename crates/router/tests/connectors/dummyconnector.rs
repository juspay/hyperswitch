#[derive(Clone, Copy)]
struct DummyConnectorTest;
impl ConnectorActions for DummyConnectorTest {}
impl utils::Connector for DummyConnectorTest {
        /// Returns the connector data for the current instance.
    fn get_data(&self) -> types::api::ConnectorData {
        use router::connector::DummyConnector;
        types::api::ConnectorData {
            connector: Box::new(&DummyConnector::<1>),
            connector_name: types::Connector::DummyConnector1,
            get_token: types::api::GetToken::Connector,
            merchant_connector_id: None,
        }
    }

        /// This method retrieves the authentication token for the connector. It creates a new ConnectorAuthentication instance and expects a dummy connector authentication configuration. It then converts the result into the specified ConnectorAuthType using the utils::to_connector_auth_type function.
    fn get_auth_token(&self) -> types::ConnectorAuthType {
        utils::to_connector_auth_type(
            connector_auth::ConnectorAuthentication::new()
                .dummyconnector
                .expect("Missing connector authentication configuration")
                .into(),
        )
    }

        /// This method returns the name of the connector as a String.
    fn get_name(&self) -> String {
        "dummyconnector".to_string()
    }
}

static CONNECTOR: DummyConnectorTest = DummyConnectorTest {};

