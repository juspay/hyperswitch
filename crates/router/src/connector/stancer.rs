impl<Flow, Request, Response> ConnectorCommonExt<Flow, Request, Response> for Stancer
where
    Self: ConnectorIntegration<Flow, Request, Response>,
{
    fn build_headers(
        &self,
        req: &types::RouterData<Flow, Request, Response>,
        _connectors: &settings::Connectors,
    ) -> CustomResult<Vec<(String, request::Maskable<String>)>, errors::ConnectorError> {
        let mut header = vec![(
            headers::CONTENT_TYPE.to_string(),
            self.get_content_type().to_string().into(),
        )];
        let mut api_key = self.get_auth_header(&req.connector_auth_type)?;
        header.append(&mut api_key);
        Ok(header)
    }
}

impl ConnectorCommon for Stancer {
    fn id(&self) -> &'static str {
        "stancer"
    }

    fn get_currency_unit(&self) -> api::CurrencyUnit {
        api::CurrencyUnit::Minor
    }

    fn common_get_content_type(&self) -> &'static str {
        "application/json"
    }

    fn base_url<'a>(&self, connectors: &'a settings::Connectors) -> &'a str {
        connectors.stancer.base_url.as_ref()
    }

    fn get_auth_header(
        &self,
        auth_type: &types::ConnectorAuthType,
    ) -> CustomResult<Vec<(String, request::Maskable<String>)>, errors::ConnectorError> {
        let auth = stancer::StancerAuthType::try_from(auth_type)
            .change_context(errors::ConnectorError::FailedToObtainAuthType)?;

        Ok(vec![(
            headers::AUTHORIZATION.to_string(),
            format!(
                "Basic {}",
                consts::BASE64_ENGINE.encode(format!("{}:", auth.api_key.peek()))
            )
            .into_masked(),
        )])
    }

    fn build_error_response(
        &self,
        res: Response,
    ) -> CustomResult<ErrorResponse, errors::ConnectorError> {
        let response: stancer::StancerErrorResponse = res
            .response
            .parse_struct("StancerErrorResponse")
            .change_context(errors::ConnectorError::ResponseDeserializationFailed)?;
        let stancer::StancerErrorResponse::Error {
            message,
            error_type,
        } = response;
        let payment_id = message["id"].to_string();

        Ok(ErrorResponse {
            status_code: res.status_code,
            code: error_type,
            message: message.to_string(),
            reason: Some(message.to_string()),
            attempt_status: None,
            connector_transaction_id: if payment_id.starts_with("paym_") {
                Some(payment_id)
            } else {
                None
            },
        })
    }
}

impl ConnectorValidation for Stancer {
    fn validate_capture_method(
        &self,
        capture_method: Option<enums::CaptureMethod>,
    ) -> CustomResult<(), errors::ConnectorError> {
        let capture_method = capture_method.unwrap_or_default();
        match capture_method {
            enums::CaptureMethod::Automatic | enums::CaptureMethod::Manual => Ok(()),
            enums::CaptureMethod::ManualMultiple | enums::CaptureMethod::Scheduled => {
                Err(errors::ConnectorError::NotSupported {
                    message: capture_method.to_string(),
                    connector: self.id(),
                }
                .into())
            }
        }
    }
}

